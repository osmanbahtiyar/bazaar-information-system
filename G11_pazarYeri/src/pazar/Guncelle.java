    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String tc, tel, cal_tezgah, sahip;
        tc = tc_no.getSelectedItem().toString();
        tel = yeni_tel.getText();
        cal_tezgah = yeni_tezgah.getSelectedItem().toString();
        sahip = yeni_sahip.getSelectedItem().toString();
        try{
            
            String query = "UPDATE calisanlar SET ";
            if(!cal_tezgah.equals("Yok")){
                query += "calistigi_tezgah = " + Integer.parseInt(cal_tezgah) + " ,";
            }
            if(!sahip.equals("Yok")){
                query += "sahibi_oldugu_tezgah = " + Integer.parseInt(sahip) + " ,";
            }
            tel = tel.trim();
            if(tel.startsWith("+90")){
                if(tel.length() > 3){
                    tel = tel.substring(3);
                    long i_tel = Long.parseLong(tel);
                    if(tel.length() != 10){
                        throw new NumberFormatException();
                    }
                    query += "tel_no = " + i_tel + " ,";
                }
            }
            
            query = query.substring(0, query.length()-1);
            query += " WHERE tc = ?";
            try (PreparedStatement p = conn.prepareStatement(query)) {
                p.setString(1,tc);
                p.executeUpdate();
                String msg = "Çalışan Güncellendi";
                JOptionPane.showMessageDialog(this, msg);
            }
            
        } catch(NumberFormatException e){
            String msg = "Telefon No Formatı Yanlış!";
            JOptionPane.showMessageDialog(this, msg);
            
        } catch (SQLException ex) {
            Logger.getLogger(Guncelle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

