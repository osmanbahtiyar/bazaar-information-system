    private void doldur(){
        tezgah_no.removeAllItems();
        tc_no.removeAllItems();
        urun_ad.removeAllItems();
        sat_tezgah.removeAllItems();
        ArrayList<Integer[]> tezgahlar = new ArrayList<>();
        ArrayList<Object[]> calisanlar = new ArrayList<>();
        ArrayList<Object[]> urunler = new ArrayList<>();
        ArrayList<Integer> tezgahNolar = new ArrayList<>();
        try {
            tezgahlar = Database.getTezgahlar();
            calisanlar = Database.getCalisanlar();
            urunler = Database.getUrunler();
        } catch (SQLException ex) {
            Logger.getLogger(Ekle.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i = 0; i < tezgahlar.size(); i++) {
            Integer[] tezgah = new Integer[4];
            tezgah = tezgahlar.get(i);
            tezgahNolar.add(tezgah[0]);
        }
        Collections.sort(tezgahNolar);
        yeni_tezgah.addItem("Yok");
        yeni_sahip.addItem("Yok");
        for (int i = 0; i < tezgahNolar.size(); i++) {
            tezgah_no.addItem(String.valueOf(tezgahNolar.get(i)));
            yeni_tezgah.addItem(String.valueOf(tezgahNolar.get(i)));
            yeni_sahip.addItem(String.valueOf(tezgahNolar.get(i)));
        }
        for (int i = 0; i < calisanlar.size(); i++) {
            Object[] calisan = new Object[8];
            calisan = calisanlar.get(i);
            tc_no.addItem(String.valueOf(calisan[1]));
        }
        for (int i = 0; i < urunler.size(); i++) {
            Object[] urun = new Object[6];
            urun = urunler.get(i);
            urun_ad.addItem(String.valueOf(urun[3]));
            sat_tezgah.addItem(String.valueOf(urun[1]));
        }
    }
