    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tezgah,alan, kira, stok;
        tezgah = tezgah_no.getSelectedItem().toString();
        alan = yeni_alan.getText();
        kira = yeni_kira.getText();
        stok = yeni_stok.getText();
        try{
            int i_tezgah = Integer.parseInt(tezgah);
            String query = "UPDATE tezgahlar SET ";
            if(alan.length() > 0){
                int i_alan = Integer.parseInt(alan);
                if(i_alan < 0){
                    throw new NumberFormatException();
                }
                query += "alan = " + i_alan + " ,";
            }
            if(kira.length() > 0){
                int i_kira = Integer.parseInt(kira);
                if(i_kira < 0){
                    throw new NumberFormatException();
                }
                query += " kira = " + i_kira + " ,";
            }
            if(stok.length() > 0){
                int i_stok = Integer.parseInt(stok);
                if(i_stok < 0){
                    throw new NumberFormatException();
                }
                query += " max_stok = " + i_stok + " ,";
            }
            query = query.substring(0,query.length()-1);
            query += " WHERE tezgah_no = ?";
            try (PreparedStatement p = conn.prepareStatement(query)) {
                p.setInt(1, Integer.parseInt(tezgah));
                p.executeUpdate();
                String msg = "Tezgah Güncellendi";
                JOptionPane.showMessageDialog(this, msg);
            }
            
        } catch(NumberFormatException e){
            String msg = "Alan, kira ve stok değerleri tamsayı ve 0'dan büyük olmalıdır!";
            JOptionPane.showMessageDialog(this, msg);
            
        } catch (SQLException ex) {
            Logger.getLogger(Guncelle.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

