    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String tur, isim, fiyat, stok, tezgah;
        tur = urun_tur.getText();
        isim = urun_ad.getText();
        fiyat = urun_fiyat.getText();
        stok = urun_stok.getText();
        tezgah = urun_tezgah.getSelectedItem().toString();
        try{
            if(!tur.matches("^[a-zA-Z]*$")){
                throw new NumberFormatException();
            }
            if(isim.length() < 1){
                throw new NumberFormatException();
            }
            float i_fiyat = Float.parseFloat(fiyat);
            int i_stok = Integer.parseInt(stok);
            String query = "INSERT INTO urunler VALUES(nextval('urun_id_olustur'),"
                    + "?,?,?,?,?)";
            try (PreparedStatement p = conn.prepareStatement(query)) {
                p.setInt(1, Integer.parseInt(tezgah));
                p.setString(2, tur);
                p.setString(3, isim);
                p.setFloat(4, i_fiyat);
                p.setInt(5, i_stok);
                p.executeUpdate();
            }
            String msg = "Ürün eklendi. (Trigger ile max stok kontrol edildi.)";
            JOptionPane.showMessageDialog(this, msg);
            
        } catch(NumberFormatException e){
            String msg = "Girdiğiniz Değerleri Kontrol Edin!";
            JOptionPane.showMessageDialog(this, msg);
        } catch(PSQLException e){
            String msg = "Ürünü Eklemeye Çalıştığınız Tezgahın Maksimum Stoğunu Aştınız. (Trigger ile kontrol edildi.)";
            JOptionPane.showMessageDialog(this, msg);
        }
        catch (SQLException ex) {
            Logger.getLogger(Ekle.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

