    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String tc, tel, isim, soyisim, cinsiyet, calistigi_tezgah, sahip,dg;
        tc = calisan_tc.getText();
        tel = calisan_tel.getText();
        isim = calisan_isim.getText();
        soyisim = calisan_soyisim.getText();
        dg = calisan_dg.getText();
        cinsiyet = cb_cinsiyet.getSelectedItem().toString();
        calistigi_tezgah = calisan_tezgah.getSelectedItem().toString();
        sahip = calisan_tezgah2.getSelectedItem().toString();
        try{
            long i_tc = Long.parseLong(tc);
            if(tc.length() != 11){
                throw new NumberFormatException();
            }
            if(tel.startsWith("+90")){
                tel = tel.trim();
                tel = tel.substring(3);
                long i_tel = Long.parseLong(tel);
                if(tel.length() != 10){
                    throw new NumberFormatException();
                }
            }
            if(isim.length() < 1 || soyisim.length() < 1){
                throw new NumberFormatException();
            }
            if(!dg.matches("\\d{4}-\\d{2}-\\d{2}")){
                throw new NumberFormatException();
            }
            switch (cinsiyet) {
                case "Erkek":
                    cinsiyet = "E";
                    break;
                case "Kadın":
                    cinsiyet = "K";
                    break;
                default:
                    cinsiyet = "B";
                    break;
            }
            int c_tezgah = Integer.parseInt(calistigi_tezgah);
            String query = "INSERT INTO calisanlar VALUES(nextval('calisan_id_olustur'),"
                    + "?,?,?,?,?,?,?,?)";
            try (PreparedStatement p = conn.prepareStatement(query)) {
                if(sahip.equals("Sahip olduğu tezgah yok.")){
                    p.setNull(8, java.sql.Types.NULL);
                }
                else{
                    int i_sahip = Integer.parseInt(sahip);
                    p.setInt(8, i_sahip);
                }
                Date date = new Date(0);
                p.setString(1,tc);
                p.setString(2,isim);
                p.setString(3,soyisim);
                p.setString(4,tel);
                p.setString(5,cinsiyet);
                p.setDate(6, date.valueOf(dg));
                p.setInt(7,c_tezgah);
                p.executeUpdate();
            }
            String msg = "Çalışan eklendi";
            JOptionPane.showMessageDialog(this, msg);
            
        }catch(NumberFormatException e){
            String msg = "Girdiğiniz Değerleri Kontrol Edin!";
            JOptionPane.showMessageDialog(this, msg);
        } catch (SQLException ex) {
            Logger.getLogger(Ekle.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

