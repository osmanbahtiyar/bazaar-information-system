    private void doldur(){
        calisan_tezgah.removeAllItems();
        calisan_tezgah2.removeAllItems();
        urun_tezgah.removeAllItems();
        ArrayList<Integer[]> tezgahlar = new ArrayList<>();
        try {
            tezgahlar = Database.getTezgahlar();
        } catch (SQLException ex) {
            Logger.getLogger(Ekle.class.getName()).log(Level.SEVERE, null, ex);
        }
        calisan_tezgah2.addItem("Sahip olduğu tezgah yok.");
        for (int i = 0; i < tezgahlar.size(); i++) {
            Integer[] tezgah = new Integer[4];
            tezgah = tezgahlar.get(i);
            calisan_tezgah.addItem(String.valueOf(tezgah[0]));
            calisan_tezgah2.addItem(String.valueOf(tezgah[0]));
            urun_tezgah.addItem(String.valueOf(tezgah[0]));
        }
    }
